/**
 * Generate AWS Signature V4 for Wasabi/S3 authentication
 */
async function signRequest(method, url, headers = {}, body = null) {
    // For presigned URLs or direct access, we use simpler approach
    // This worker can either:
    // 1. Proxy requests with AWS Signature (recommended for security)
    // 2. Use presigned URLs generated by your bot (simpler)
    
    const accessKey = WASABI_CONFIG.accessKey || env.WASABI_ACCESS_KEY;
    const secretKey = WASABI_CONFIG.secretKey || env.WASABI_SECRET_KEY;
    
    if (!accessKey || !secretKey) {
        console.error("Missing Wasabi credentials");
        return null;
    }
    
    // Parse URL
    const parsedUrl = new URL(url);
    const region = WASABI_CONFIG.region || env.WASABI_REGION || 'us-east-1';
    const service = 's3';
    const host = parsedUrl.hostname;
    
    const now = new Date();
    const amzDate = now.toISOString().replace(/[:\-]|\.\d{3}/g, '');
    const dateStamp = amzDate.slice(0, 8);
    
    // Create canonical request
    const canonicalHeaders = `host:${host}\nx-amz-date:${amzDate}\n`;
    const signedHeaders = 'host;x-amz-date';
    
    let canonicalRequest = `${method}\n${parsedUrl.pathname}\n${parsedUrl.searchParams.toString()}\n${canonicalHeaders}\n${signedHeaders}\nUNSIGNED-PAYLOAD`;
    
    // Create string to sign
    const algorithm = 'AWS4-HMAC-SHA256';
    const credentialScope = `${dateStamp}/${region}/${service}/aws4_request`;
    const stringToSign = `${algorithm}\n${amzDate}\n${credentialScope}\n${await sha256(canonicalRequest)}`;
    
    // Calculate signature
    const signature = await calculateSignature(secretKey, dateStamp, region, service, stringToSign);
    
    // Create authorization header
    const authorization = `${algorithm} Credential=${accessKey}/${credentialScope}, SignedHeaders=${signedHeaders}, Signature=${signature}`;
    
    return {
        'Authorization': authorization,
        'x-amz-date': amzDate,
        'Host': host
    };
}

/**
 * SHA256 hash helper
 */
async function sha256(message) {
    const encoder = new TextEncoder();
    const data = encoder.encode(message);
    const hash = await crypto.subtle.digest('SHA-256', data);
    return Array.from(new Uint8Array(hash))
        .map(b => b.toString(16).padStart(2, '0'))
        .join('');
}

/**
 * HMAC-SHA256 calculation for AWS signature
 */
async function hmacSha256(key, message) {
    const encoder = new TextEncoder();
    const keyData = typeof key === 'string' ? encoder.encode(key) : key;
    const messageData = encoder.encode(message);
    const cryptoKey = await crypto.subtle.importKey('raw', keyData, { name: 'HMAC', hash: 'SHA-256' }, false, ['sign']);
    const signature = await crypto.subtle.sign('HMAC', cryptoKey, messageData);
    return new Uint8Array(signature);
}

async function calculateSignature(secretKey, dateStamp, region, service, stringToSign) {
    const kSecret = `AWS4${secretKey}`;
    const kDate = await hmacSha256(kSecret, dateStamp);
    const kRegion = await hmacSha256(kDate, region);
    const kService = await hmacSha256(kRegion, service);
    const kSigning = await hmacSha256(kService, 'aws4_request');
    const signature = await hmacSha256(kSigning, stringToSign);
    return Array.from(signature).map(b => b.toString(16).padStart(2, '0')).join('');
}

/**
 * Generate a presigned URL for direct Wasabi access
 */
async function generatePresignedUrl(key, expiresIn = 3600) {
    const accessKey = WASABI_CONFIG.accessKey || env.WASABI_ACCESS_KEY;
    const secretKey = WASABI_CONFIG.secretKey || env.WASABI_SECRET_KEY;
    const bucket = WASABI_CONFIG.bucket || env.WASABI_BUCKET;
    const region = WASABI_CONFIG.region || env.WASABI_REGION || 'us-east-1';
    const endpoint = WASABI_CONFIG.endpoint || 'https://s3.wasabisys.com';
    
    if (!accessKey || !secretKey) {
        return null;
    }
    
    const method = 'GET';
    const host = `${bucket}.s3.${region}.wasabisys.com`;
    const url = new URL(`https://${host}/${encodeURIComponent(key)}`);
    
    const now = new Date();
    const amzDate = now.toISOString().replace(/[:\-]|\.\d{3}/g, '');
    const dateStamp = amzDate.slice(0, 8);
    const expires = expiresIn;
    
    // Add query parameters for presigned URL
    url.searchParams.set('X-Amz-Algorithm', 'AWS4-HMAC-SHA256');
    url.searchParams.set('X-Amz-Credential', `${accessKey}/${dateStamp}/${region}/s3/aws4_request`);
    url.searchParams.set('X-Amz-Date', amzDate);
    url.searchParams.set('X-Amz-Expires', expires.toString());
    url.searchParams.set('X-Amz-SignedHeaders', 'host');
    
    // Create canonical request for presigned URL
    const canonicalHeaders = `host:${host}\n`;
    const signedHeaders = 'host';
    const canonicalRequest = `${method}\n/${encodeURIComponent(key)}\n${url.searchParams.toString()}\n${canonicalHeaders}\n${signedHeaders}\nUNSIGNED-PAYLOAD`;
    
    const stringToSign = `AWS4-HMAC-SHA256\n${amzDate}\n${dateStamp}/${region}/s3/aws4_request\n${await sha256(canonicalRequest)}`;
    
    const signature = await calculateSignature(secretKey, dateStamp, region, 's3', stringToSign);
    url.searchParams.set('X-Amz-Signature', signature);
    
    return url.toString();
}

/**
 * Check if file is a video based on extension
 */
function isVideoFile(filename) {
    const ext = filename.toLowerCase().substring(filename.lastIndexOf('.'));
    return SUPPORTED_VIDEO_FORMATS.includes(ext);
}

/**
 * Format bytes to human readable
 */
function formatBytes(bytes) {
    if (bytes === 0) return '0 Bytes';
    const k = 1024;
    const sizes = ['Bytes', 'KB', 'MB', 'GB', 'TB'];
    const i = Math.floor(Math.log(bytes) / Math.log(k));
    return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
}

/**
 * Extract filename from path or query
 */
function getFilenameFromPath(pathname) {
    const decoded = decodeURIComponent(pathname);
    const parts = decoded.split('/');
    let filename = parts.pop() || 'video';
    // Remove query params if any
    filename = filename.split('?')[0];
    return filename;
}

