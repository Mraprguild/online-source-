function escapeHtml(str) {
    if (!str) return '';
    return str.replace(/[&<>]/g, function(m) {
        if (m === '&') return '&amp;';
        if (m === '<') return '&lt;';
        if (m === '>') return '&gt;';
        return m;
    }).replace(/[\uD800-\uDBFF][\uDC00-\uDFFF]/g, function(c) {
        return c;
    });
}

// ============ MAIN WORKER HANDLER ============

export default {
    async fetch(request, env, ctx) {
        const url = new URL(request.url);
        const path = url.pathname;
        
        // Handle CORS preflight
        if (request.method === 'OPTIONS') {
            return new Response(null, { headers: CORS_HEADERS });
        }
        
        // Health check endpoint
        if (path === '/health' || path === '/') {
            return new Response(JSON.stringify({
                status: 'healthy',
                service: 'wasabi-stream-worker',
                timestamp: new Date().toISOString()
            }), {
                headers: { 'Content-Type': 'application/json', ...CORS_HEADERS }
            });
        }
        
        // Player route: /player/:encodedUrl or /player?url=...
        if (path.startsWith('/player') || path === '/watch') {
            let videoUrl = null;
            let filename = url.searchParams.get('name') || 'video.mp4';
            
            // Check for encoded URL parameter
            if (url.searchParams.has('url')) {
                videoUrl = decodeURIComponent(url.searchParams.get('url'));
            } 
            // Check for path-based encoded URL: /player/encodedBase64Url
            else if (path.startsWith('/player/')) {
                const encoded = path.substring(8);
                try {
                    // Support base64url encoded strings
                    let base64 = encoded.replace(/-/g, '+').replace(/_/g, '/');
                    while (base64.length % 4) base64 += '=';
                    videoUrl = atob(base64);
                } catch (e) {
                    videoUrl = decodeURIComponent(encoded);
                }
            }
            
            if (!videoUrl) {
                return new Response(getErrorPage('Missing video URL parameter'), {
                    headers: { 'Content-Type': 'text/html' },
                    status: 400
                });
            }
            
            // Try to extract filename from URL if not provided
            if (filename === 'video.mp4' && videoUrl) {
                const urlParts = videoUrl.split('/');
                const lastPart = urlParts.pop().split('?')[0];
                if (lastPart && lastPart.includes('.')) {
                    filename = decodeURIComponent(lastPart);
                }
            }
            
            const html = getPlayerHTML(videoUrl, filename);
            return new Response(html, {
                headers: { 'Content-Type': 'text/html; charset=utf-8' }
            });
        }
        
        // Stream proxy endpoint: /stream/:key - proxies file with proper range support
        if (path.startsWith('/stream/')) {
            const key = decodeURIComponent(path.substring(8));
            const accessKey = env.WASABI_ACCESS_KEY || WASABI_CONFIG.accessKey;
            const secretKey = env.WASABI_SECRET_KEY || WASABI_CONFIG.secretKey;
            const bucket = env.WASABI_BUCKET || WASABI_CONFIG.bucket;
            const region = env.WASABI_REGION || WASABI_CONFIG.region;
            
            if (!accessKey || !secretKey) {
                return new Response('Storage not configured', { status: 500 });
            }
            
            const host = `${bucket}.s3.${region}.wasabisys.com`;
            const s3Url = `https://${host}/${encodeURIComponent(key)}`;
            
            // Forward range headers for video seeking
            const rangeHeader = request.headers.get('Range');
            const headers = {
                'Host': host
            };
            
            // Simple fetch without full signature for public bucket or use presigned
            // For private buckets, you'd need full signature. This example assumes
            // the worker has access or uses presigned URLs from bot
            
            const s3Request = new Request(s3Url, {
                method: 'GET',
                headers: rangeHeader ? { 'Range': rangeHeader } : {},
                cf: { cacheTtl: 3600, cacheEverything: true }
            });
            
            try {
                const response = await fetch(s3Request);
                const newHeaders = new Headers(response.headers);
                newHeaders.set('Access-Control-Allow-Origin', '*');
                newHeaders.set('Access-Control-Expose-Headers', 'Content-Range, Accept-Ranges');
                
                return new Response(response.body, {
                    status: response.status,
                    headers: newHeaders
                });
            } catch (error) {
                return new Response(`Stream error: ${error.message}`, { status: 500 });
            }
        }
        
        // Generate presigned URL endpoint (requires auth)
        if (path === '/api/presign' && request.method === 'POST') {
            // Add your authentication check here (e.g., header token)
            const authHeader = request.headers.get('X-Auth-Token');
            if (authHeader !== env.API_TOKEN && authHeader !== 'your-secret-token') {
                return new Response('Unauthorized', { status: 401 });
            }
            
            try {
                const body = await request.json();
                const key = body.key;
                const expires = body.expires || 604800; // 7 days default
                
                if (!key) {
                    return new Response(JSON.stringify({ error: 'Missing key' }), { status: 400 });
                }
                
                const presignedUrl = await generatePresignedUrl(key, expires);
                if (presignedUrl) {
                    return new Response(JSON.stringify({ url: presignedUrl, expires }), {
                        headers: { 'Content-Type': 'application/json' }
                    });
                } else {
                    return new Response(JSON.stringify({ error: 'Failed to generate URL' }), { status: 500 });
                }
            } catch (e) {
                return new Response(JSON.stringify({ error: e.message }), { status: 500 });
            }
        }
        
        // Default 404
        return new Response('Not Found', { status: 404 });
    }
};

